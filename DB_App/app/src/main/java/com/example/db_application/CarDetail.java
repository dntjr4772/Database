package com.example.db_application;

public class CarDetail {
    String name;
    String carcol;
    String type;
    String company;
    String passengers;
    String efficiency;

    public CarDetail(String name, String carcol, String type, String company, String passengers, String efficiency) {
        this.name=name;
        this.carcol=carcol;
        this.type=type;
        this.company=company;
        this.passengers=passengers;
        this.efficiency=efficiency;
    }

}
